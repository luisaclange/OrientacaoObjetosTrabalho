    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // boto salvar
        int lib = 0;
        String msg = "Os Campos: \n";

        if (txtnome.getText().equals("")) {//campo nome vazio
            msg += "\n Nome ";
            lib++;
        }

        if (lib == 0) {

            Feedback obj = new Feedback();
            ClientesDAO cd = new ClientesDAO();
            Date agora = new Date();
            SimpleDateFormat dataBr = new SimpleDateFormat("dd/MM/yyyy");
            String dataformatada = dataBr.format(agora);

            obj.setCliente(cd.buscaporid(Integer.parseInt(txtid.getText())));
            obj.setData_feedback(dataformatada);
            obj.setDescricao(txtdescricao.getText());

            FeedbackDAO dao = new FeedbackDAO();

            dao.cadastrarFeedback(obj);

            new Utilitarios().LimpaTela(painel_dados);

        } else {
            msg += "\n\n Estão Vazios.";
            JOptionPane.showMessageDialog(null, msg, "ERRO AO CADASTRAR ", HEIGHT);
        }
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // botao editar

        Feedback obj = new Feedback();
        SimpleDateFormat dataBr = new SimpleDateFormat("dd/MM/yyyy");
        Clientes c = new Clientes();
        ClientesDAO cd = new ClientesDAO();

        obj.setDescricao(txtdescricao.getText());
        obj.setData_feedback(dataBr.format(LocalDateTime.now().toString()));
        obj.setCliente(cd.buscaporid(Integer.parseInt(txtid.getText())));

        FeedbackDAO dao = new FeedbackDAO();

        dao.alterarFeedback(obj);

        new Utilitarios().LimpaTela(painel_dados);
    }//GEN-LAST:event_btneditarActionPerformed

